

//電腦版篩選器
function changeToAll(){
    //先取得要操作的標籤
    let allDiv=document.querySelector(".all-info");
    let toysDiv=document.querySelector(".toys-info");
    let livingDiv=document.querySelector(".living-info");
    let vintageDiv=document.querySelector(".vintage-info");
    let photoDiv=document.querySelector(".photos-info");
    let pageDiv=document.querySelector(".page");
    let allMenu=document.querySelector(".all-menu>span");
    let toysMenu=document.querySelector(".toys-menu>span");
    let livingMenu=document.querySelector(".living-menu>span");
    let vintageMenu=document.querySelector(".vintage-menu>span");
    let photosMenu=document.querySelector(".photos-menu>span");

    //在對標籤做操作
    allDiv.style.display="block grid";
    toysDiv.style.display="none";
    livingDiv.style.display="none";
    vintageDiv.style.display="none";
    photoDiv.style.display="none";
    pageDiv.style.display="block";

    allMenu.style.color="#FE793E";
    toysMenu.style.color="#3A3A3A";
    livingMenu.style.color="#3A3A3A";
    vintageMenu.style.color="#3A3A3A";
    photosMenu.style.color="#3A3A3A";
}

